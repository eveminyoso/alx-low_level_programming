#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - returns sum
 * @a: first number
 * @b: second number
 * Return: a plus b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns substraction
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns multiplication
 * @a: first number
 * @b: second number
 * Return: a times b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns division
 * @a: first number
 * @b: second number
 * Return: a divided by b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns modulus
 * @a: first number
 * @b: second number
 * Return: a modulus b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

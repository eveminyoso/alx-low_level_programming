#include <stdio.h>
/**
 *add - adds a and b
 *@a:first number
 *@b:second number
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 *sub - subtracts a and b
 *@a:first number
 *@b:second number
 *Return:a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 *mul - multiplies a and b
 *@a:first number
 *@b:second number
 *Return:a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 *div - divides a and b
 *@a:first number
 *@b:second number
 *Return:a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 *mod - modulus a and b
 *@a:first number
 *@b:second number
 *Return:a % b
 *
 */
int mod(int a, int b)
{
	return (a % b);
}
